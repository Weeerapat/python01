#for จะเป็น definite loop หรือ loop ที่มีการทำงานชัดเจน
#forกับ
for i in range(1,11,1):
    print(i)
print('finish')
#for and list
list1 = ["apple","blueberry","kiwi","papaya"]
for element in list1:
    print(element)
#for and dic
dic1 ={'name':'Weerapat ','age':17,'hobbies':'Read a book'}
for key,value in dic1.items():
    print(key,value)
#while indefinite loop หรือ loopที่ทำงานไม่ชัดเจน
i = 1
while i<10:
    print(i)        
    i +=1
